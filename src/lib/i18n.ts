export type Lang = 'en' | 'kh'

function getLang(): Lang {
  try {
    const stored = localStorage.getItem(STORAGE_KEY)
    if (stored === 'en' || stored === 'kh') return stored
  } catch {}
  const nav = (navigator.language || 'en').toLowerCase()
  return nav.startsWith('km') || nav.startsWith('kh') ? 'kh' : 'en'
}

function setLang(lang: Lang) {
  try { localStorage.setItem(STORAGE_KEY, lang) } catch {}
}

function t(key: string, vars?: Record<string, string | number>): string {
  const lang = getLang()
  const dict = STRINGS[lang] || STRINGS.en
  let value = dict[key]
  if (value === undefined) value = STRINGS.en[key] || key
  if (!vars) return value
  return value.replace(/\{(\w+)\}/g, (_, k) => {
    const v = vars[k]
    return v !== undefined && v !== null ? String(v) : `{${k}}`
  })
}

export const i18n = { t, getLang, setLang }
